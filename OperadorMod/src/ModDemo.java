
public class ModDemo {

	public static void main(String[] args) {

		int iresult, imod;
		double dresult, dmod;
		
		iresult = 10 / 3;
		imod = 10 % 3;
		
		dresult = 10.0 / 3.0;
		dmod = 10.0 % 3.0;
		
		System.out.println("Resultado div inteiro: " + iresult + " mod " + imod);
		System.out.println("Resultado div double: " + dresult + " mod " + dmod);		

	}

}
