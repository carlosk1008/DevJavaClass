package pro.br.carlos.instrucoescontrole.ifswitch;

public class ifswitch {

	public static void main(String[] args) {
		
		for(int i = 0; i < 4; i++) {
			
			switch(i) {
				case 0:
					System.out.println("zero");
					break;
				case 1:
					System.out.println("um");
					break;
				case 2:
					System.out.println("dois");
					break;
				case 3:
					System.out.println("três");
					break;
				default:
					System.out.println("três");					

			}
			
		}	
		
	}

}
